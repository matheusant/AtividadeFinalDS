    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        Connection con = Conexao.abrirConexao();
        ClienteBean cb = new ClienteBean();
        ClienteDAO cd = new ClienteDAO(con);

        cb.setNome(txtNome.getText());
        cb.setTelResidencial(txtTelRes.getText());
        cb.setTelComercial(txtTelCom.getText());
        cb.setTelCelular(txtTelCel.getText());
        cb.setEmail(txtEmail.getText());

        JOptionPane.showMessageDialog(null, cd.inserirCliente(cb));

        limpaCampos();

        Conexao.fecharConexao(con);

        this.tabelaClientes("select * from tbclientes");

        btnInserir.setEnabled(true);
        btnConfirmar.setEnabled(false);
        btnCancelar.setEnabled(false);
    }//GEN-LAST:event_btnConfirmarActionPerformed

