    public void tabelaClientes(String sql) {
        Connection con = Conexao.abrirConexao();

        ClienteDAO cd = new ClienteDAO(con);

        List<ClienteBean> listaCliente = new ArrayList<ClienteBean>();

        listaCliente = cd.listarTodos();

        DefaultTableModel tbm = (DefaultTableModel) tblClientes.getModel();

        for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
            tbm.removeRow(i);
        }

        int i = 0;

        for (ClienteBean cb : listaCliente) {
            tbm.addRow(new String[1]);

            tblClientes.setValueAt(cb.getId(), i, 0);
            tblClientes.setValueAt(cb.getNome(), i, 1);
            tblClientes.setValueAt(cb.getEmail(), i, 2);

            i++;
        }

        Conexao.fecharConexao(con);

    }

