    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        Connection con = Conexao.abrirConexao();
        ClienteBean cb = new ClienteBean();
        ClienteDAO cd = new ClienteDAO(con);

        cb.setId(Integer.parseInt(txtId.getText()));

        Object[] opcoes = {"Sim", "Não"};
        int i = JOptionPane.showOptionDialog(null, "Deseja realmente excluir esse cliente?\n"
                + "ID: " + txtId.getText()
                + "\nNome: " + txtNome.getText()
                + "\nE-Mail: " + txtEmail.getText(),
                 "Exclusão", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                 null, opcoes, opcoes[0]);
        if (i == JOptionPane.YES_NO_OPTION) {
            JOptionPane.showMessageDialog(null, cd.deletarCliente(cb));
        }

        Conexao.fecharConexao(con);
        limpaCampos();
        btnInserir.setEnabled(true);
        btnCancelar.setEnabled(false);
        btnRemover.setEnabled(false);
        btnAlternar.setEnabled(false);
        this.tabelaClientes("select * from tbclientes");
    }//GEN-LAST:event_btnRemoverActionPerformed

