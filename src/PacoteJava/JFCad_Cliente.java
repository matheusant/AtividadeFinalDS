    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        Connection con = Conexao.abrirConexao();
        ClienteBean cb = new ClienteBean();
        
        String sql = "select * from tbclientes where id = ?";
        
        Integer linha = tblClientes.getSelectedRow();
        
        int id = (int) tblClientes.getValueAt(linha, 0);
        
        //cb.setId((int) tblClientes.getValueAt(linha, 0));
        
        
        try {
            
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, id);
            ResultSet rs = ps.executeQuery();
            rs.next();
            
            txtId.setText(String.valueOf(rs.getInt("id")));
            txtNome.setText(rs.getString("nomeCli"));
            txtTelRes.setText(rs.getString("telResidencial"));
            txtTelCom.setText(rs.getString("telComercial"));
            txtTelCel.setText(rs.getString("telCelular"));
            txtEmail.setText(rs.getString("emailCli"));
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro");
        }
        
        
        Conexao.fecharConexao(con);

        btnAlternar.setEnabled(true);
        btnRemover.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnInserir.setEnabled(false);
    }//GEN-LAST:event_tblClientesMouseClicked

